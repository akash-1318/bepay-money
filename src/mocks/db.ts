export const db = {
    getTransactions: (): Transaction[] => {
        const data = localStorage.getItem('bepay_transactions');
        if (!data || JSON.parse(data).length < INITIAL_TRANSACTIONS.length) {
            localStorage.setItem('bepay_transactions', JSON.stringify(INITIAL_TRANSACTIONS));
            return INITIAL_TRANSACTIONS;
        }
        return JSON.parse(data);
    },

    saveTransactions: (transactions: Transaction[]) => {
        localStorage.setItem('bepay_transactions', JSON.stringify(transactions));
    },

    getPaymentLinks: (): PaymentLink[] => {
        const data = localStorage.getItem('bepay_payment_links');
        if (!data || JSON.parse(data).length < INITIAL_PAYMENT_LINKS.length) {
            localStorage.setItem('bepay_payment_links', JSON.stringify(INITIAL_PAYMENT_LINKS));
            return INITIAL_PAYMENT_LINKS;
        }
        return JSON.parse(data);
    },

    savePaymentLinks: (links: PaymentLink[]) => {
        localStorage.setItem('bepay_payment_links', JSON.stringify(links));
    }
};
